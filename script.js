function handleModuleClick(e, num, videoId, title, desc) {
  if (
    e.target.closest(".module-complete-btn") ||
    e.target.closest(".module-play")
  )
    return;
  openModuleVideo(e, num, videoId, title, desc);
}

function openModuleVideo(e, num, videoId, title, desc) {
  if (e) e.preventDefault();
  if (!currentUser) {
    showMandatoryOverlay();
    return;
  }
  openVideoModal(videoId, title, desc);
  watchedMods.add(num);
  const btn = document.getElementById("mbtn-" + num);
  if (btn && !btn.classList.contains("done")) btn.disabled = false;
}

async function handleCompleteBtn(e, num) {
  e.preventDefault();
  e.stopPropagation();
  if (!currentUser) {
    showMandatoryOverlay();
    return;
  }
  if (completedMods.has(num)) return;
  if (!watchedMods.has(num)) return;
  const sb = getSupabase();
  try {
    const { error } = await sb.from("module_progress").upsert(
      {
        user_id: currentUser.id,
        module_number: num,
        role: currentRole,
        completed: true,
        completed_at: new Date().toISOString(),
      },
      { onConflict: "user_id,module_number,role" },
    );
    if (!error) {
      completedMods.add(num);
      const btn = document.getElementById("mbtn-" + num);
      if (btn) {
        btn.textContent = "✅ Пройден";
        btn.classList.add("done");
        btn.disabled = false;
      }
      updateProgressBar();
      checkCertificate();
    }
  } catch (e) {
    console.warn("toggleModule error:", e);
  }
}


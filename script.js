function resetQuizFromBanner() {
  clearQuizState();
  const banner = document.getElementById('quizResumeBanner');
  if (banner) banner.remove();
  quizCurrent = 0;
  quizScore = 0;
  quizAnswered = false;
  moduleScores = {};
  quizAnswers = [];
  quizQuestions.forEach(q => {
    if (!moduleScores[q.module]) moduleScores[q.module] = { correct: 0, total: 0 };
  });
  renderQuestion();
}

function renderQuestion() {
  if (!quizQuestions.length) return;
  quizAnswered = false;
  const q = quizQuestions[quizCurrent];
  const pct = (quizCurrent / quizQuestions.length) * 100;
  document.getElementById("quizProgressBar").style.width = pct + "%";
  document.getElementById("quizProgressLabel").textContent = `Вопрос ${
    quizCurrent + 1
  } из ${quizQuestions.length}`;
  document.getElementById("quizNav").style.display = "none";
  document.getElementById("quizResult").style.display = "none";

  // Перемешиваем варианты ответов
  const body = document.getElementById("quizBody");
  body.innerHTML =
    `<div class="quiz-module-tag">${q.module}</div>` +
    `<div class="quiz-question">${q.q}</div>` +
    `<div class="quiz-options" id="quizOptions">` +
    q.options
      .map(
        (o, i) => `<button class="quiz-option" data-idx="${i}">${o}</button>`,
      )
      .join("") +
    `</div>` +
    `<div class="quiz-feedback" id="quizFeedback"></div>`;

  body.querySelectorAll(".quiz-option").forEach((btn) => {
    btn.addEventListener("click", function () {
      selectOption(parseInt(this.dataset.idx));
    });
  });
}

function selectOption(idx) {
  if (!currentUser) {
    showMandatoryOverlay();
    return;
  }
  if (quizAnswered) return;
  quizAnswered = true;
  const q = quizQuestions[quizCurrent];
  if (!moduleScores[q.module])
    moduleScores[q.module] = { correct: 0, total: 0 };
  moduleScores[q.module].total++;
  const btns = document.querySelectorAll(".quiz-option");
  btns.forEach((b) => (b.disabled = true));
  if (idx === q.correct) {
    btns[idx].classList.add("correct");
  } else {
    btns.forEach((b) => {
      if (parseInt(b.dataset.idx) === q.correct) b.classList.add("wrong");
    });
  }
  if (idx === q.correct) {
    quizScore++;
    moduleScores[q.module].correct++;
  }
  const fb = document.getElementById("quizFeedback");
  fb.textContent =
    (idx === q.correct ? "✅ Верно! " : "❌ Неверно. ") + q.feedback;
  fb.className = "quiz-feedback show " + (idx === q.correct ? "ok" : "err");
  document.getElementById("quizNav").style.display = "flex";
  if (quizCurrent === quizQuestions.length - 1)
    document.getElementById("quizNext").textContent = "Посмотреть результат →";
  quizAnswers[quizCurrent] = { selected: idx, correct: q.correct };
  saveQuizState();
}

function nextQuestion() {
  if (quizCurrent < quizQuestions.length - 1) {
    quizCurrent++;
    renderQuestion();
  } else showResult();
}

async function showResult() {
  clearQuizState();
  document.getElementById("quizProgressBar").style.width = "100%";
  document.getElementById("quizProgressLabel").textContent = "Тест завершён";
  document.getElementById("quizBody").innerHTML = "";
  document.getElementById("quizNav").style.display = "none";

  const pct = Math.round((quizScore / quizQuestions.length) * 100);
  let savedPrev = previousScore;
  let savedBest = bestScore;

  if (currentUser) {
    const sb = getSupabase();
    const breakdown = {};
    Object.keys(moduleScores).forEach((m) => {
      breakdown[m] = moduleScores[m];
    });
    try {
      await sb.from("quiz_results").insert({
        user_id: currentUser.id,
        role: currentRole,
        score: quizScore,
        total: quizQuestions.length,
        percentage: pct,
        module_breakdown: breakdown,
        completed_at: new Date().toISOString(),
      });
      await loadScoreHistory();
      savedPrev = previousScore;
      savedBest = bestScore;
      await checkCertificate();
    } catch (e) {
      console.warn("quiz save error:", e);
    }
  }

  let label, color;
  if (pct >= 85) {
    label = "🏆 Отличный результат! Вы хорошо владеете основами ИБ.";
    color = "var(--success)";
  } else if (pct >= 65) {
    label = "👍 Хороший результат. Повторите слабые модули.";
    color = "#ffa500";
  } else {
    label = "📚 Стоит повторить материал и пройти тест снова.";
    color = "var(--danger)";
  }

  document.getElementById("quizScoreDisplay").textContent =
    `${quizScore} / ${quizQuestions.length}`;
  document.getElementById("quizScoreDisplay").style.color = color;
  document.getElementById("quizResultLabel").innerHTML =
    `${pct}% правильных ответов<br><span style="font-size:1rem;color:var(--text-dim)">${label}</span>`;

  if (pct === 100)
    document.getElementById("quizPerfectBanner").classList.add("show");
  else document.getElementById("quizPerfectBanner").classList.remove("show");

  document.getElementById("quizHistoryPanel").classList.add("show");
  document.getElementById("qhpCurrent").textContent =
    `${quizScore}/${quizQuestions.length}`;
  document.getElementById("qhpCurrentSub").textContent = pct + "%";

  if (savedPrev) {
    document.getElementById("qhpPrev").textContent =
      `${savedPrev.score}/${savedPrev.total}`;
    document.getElementById("qhpPrevSub").textContent =
      savedPrev.percentage + "%";
  } else {
    document.getElementById("qhpPrev").textContent = "—";
    document.getElementById("qhpPrevSub").textContent = "Первая попытка";
  }
  if (savedBest) {
    document.getElementById("qhpBest").textContent =
      `${savedBest.score}/${savedBest.total}`;
    document.getElementById("qhpBestSub").textContent =
      savedBest.percentage + "%";
  } else {
    document.getElementById("qhpBest").textContent =
      `${quizScore}/${quizQuestions.length}`;
    document.getElementById("qhpBestSub").textContent = pct + "%";
  }

  const bd = document.getElementById("quizBreakdown");
  const uniqueModules = [...new Set(quizQuestions.map((q) => q.module))];
  bd.innerHTML = uniqueModules
    .map((m) => {
      const s = moduleScores[m] || { correct: 0, total: 0 };
      const mp = s.total > 0 ? Math.round((s.correct / s.total) * 100) : 0;
      const cls = mp >= 80 ? "great" : mp >= 50 ? "ok" : "bad";
      const shortName = m.split("·")[1] ? m.split("·")[1].trim() : m;
      return `<div class="breakdown-item">
        <div class="b-module">${shortName}</div>
        <div class="b-score ${cls}">${s.correct}/${s.total} — ${mp}%</div>
      </div>`;
    })
    .join("");

  document.getElementById("quizResult").style.display = "block";
}


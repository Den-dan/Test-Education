function openVideoModal(videoId, title, desc) {
  currentVideoId = videoId;
  const modal = document.getElementById("videoModal");
  const titleEl = document.getElementById("videoModalTitle");
  const descEl = document.getElementById("videoModalDesc");
  const previewImg = document.getElementById("videoPreviewImg");
  const previewWrap = document.getElementById("videoPreviewWrap");
  const embedWrap = document.getElementById("videoEmbedWrap");
  const iframe = document.getElementById("videoModalIframe");

  titleEl.textContent = title;
  descEl.textContent = desc;

  iframe.src = "";
  embedWrap.style.display = "none";
  previewWrap.style.display = "";

  // Проверяем доступность embed через onerror на картинке
  const testImg = new Image();
  testImg.src = `https://img.youtube.com/vi/${videoId}/hqdefault.jpg`;

  previewImg.src = testImg.src;

  // Вешаем обработчик на iframe — если видео заблокировано, открываем на YouTube
  iframe.onload = function () {
    try {
      // Если iframe загрузился но показывает "недоступно" — это не поймать через onload,
      // поэтому вешаем резервную кнопку в футер
      const fallbackBtn = document.getElementById("videoFallbackBtn");
      if (fallbackBtn) {
        fallbackBtn.style.display = "inline-block";
        fallbackBtn.href = `https://www.youtube.com/watch?v=${videoId}`;
      }
    } catch (e) {}
  };

  modal.classList.add("open");
  document.body.style.overflow = "hidden";
}


async function doMaoLogin() {
  const email = document.getElementById("maoLoginEmail").value.trim();
  const password = document.getElementById("maoLoginPassword").value;
  const errEl = document.getElementById("maoLoginError");
  errEl.classList.remove("show");
  if (!email || !password) {
    errEl.textContent = "Заполните все поля.";
    errEl.classList.add("show");
    return;
  }
  const sb = getSupabase();
  const { error } = await sb.auth.signInWithPassword({ email, password });
  if (error) {
    errEl.textContent =
      error.message === "Invalid login credentials"
        ? "Неверный email или пароль."
        : error.message;
    errEl.classList.add("show");
  }
}

async function doMaoRegister() {
  const name = document.getElementById("maoRegName").value.trim();
  const email = document.getElementById("maoRegEmail").value.trim();
  const role = document.getElementById("maoRegRole").value;
  const dept = document.getElementById("maoRegDept").value.trim();
  const password = document.getElementById("maoRegPassword").value;
  const errEl = document.getElementById("maoRegError");
  const okEl = document.getElementById("maoRegSuccess");
  errEl.classList.remove("show");
  okEl.classList.remove("show");
  if (!name || !email || !password) {
    errEl.textContent = "Заполните имя, email и пароль.";
    errEl.classList.add("show");
    return;
  }
  if (password.length < 6) {
    errEl.textContent = "Пароль должен быть минимум 6 символов.";
    errEl.classList.add("show");
    return;
  }
  const sb = getSupabase();
  const { data, error } = await sb.auth.signUp({
    email,
    password,
    options: { data: { full_name: name, role: role } },
  });
if (error) {
    if (error.message.includes("already registered") || error.message.includes("User already registered")) {
      errEl.textContent = "Этот email уже зарегистрирован. Попробуйте войти.";
    } else {
      errEl.textContent = error.message;
    }
    errEl.classList.add("show");
    return;
  }
  if (data.user) {
    try {
      await sb.from("profiles").upsert(
        {
          id: data.user.id,
          email,
          full_name: name,
          role,
          department: dept,
        },
        { onConflict: "id" },
      );
    } catch (e) {
      console.warn("profile upsert error:", e);
    }
  }
  okEl.textContent = "Аккаунт создан! Входим...";
  okEl.classList.add("show");
  setTimeout(() => maoSwitchTab("login"), 1500);
}


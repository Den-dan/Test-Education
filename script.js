async function loadAllRolesData() {
  if (!currentUser) return {};
  const sb = getSupabase();
  const roles = ["employee", "manager", "it", "hr", "admin"];
  const result = {};

  try {
    const { data: modData } = await sb
      .from("module_progress")
      .select("module_number, role")
      .eq("user_id", currentUser.id)
      .eq("completed", true);

    const { data: quizData } = await sb
      .from("quiz_results")
      .select("score, total, percentage, role, completed_at")
      .eq("user_id", currentUser.id)
      .order("completed_at", { ascending: false });

    roles.forEach((role) => {
      const roleMods = (modData || []).filter((r) => r.role === role);
      const roleQuizzes = (quizData || []).filter((r) => r.role === role);
      const bestQuiz = roleQuizzes.reduce(
        (best, r) => (!best || r.percentage > best.percentage ? r : best),
        null,
      );
      result[role] = {
        modulesCompleted: roleMods.length,
        bestScore: bestQuiz ? bestQuiz.percentage : null,
        bestQuiz: bestQuiz || null,
      };
    });
  } catch (e) {
    console.warn("loadAllRolesData error:", e);
    roles.forEach((role) => {
      result[role] = { modulesCompleted: 0, bestScore: null, bestQuiz: null };
    });
  }

  return result;
}

function calculateCertLevel(allRolesData) {
  const roles = ["employee", "manager", "it", "hr", "admin"];
  const PASS_THRESHOLD = 65;

const isRolePassed = (role) => {
    const d = allRolesData[role];
    return (
      d &&
      d.bestScore !== null &&
      d.bestScore >= PASS_THRESHOLD
    );
  };

  const passedRoles = roles.filter(isRolePassed);
  const passedCount = passedRoles.length;

  const allHundred =
    passedCount === 5 &&
    roles.every((r) => allRolesData[r] && allRolesData[r].bestScore === 100);

  if (allHundred) return "crystal";
  if (passedCount === 5) return "platinum";
  if (passedCount >= 2) return "gold";
  if (passedCount >= 1) return "silver";
  return null;
}

